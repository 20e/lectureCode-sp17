public class HelloWorld {
	public static void main(String[] args) {
		System.out.println("hello world");

		int x = 5;
		x = x + 10;
		System.out.println(x);

		x = "horse" + "fish";
	}
}

/* 
1. All code in Java must be part of a class.
2. For a program to run, we must "define a main method".
3. Expressions must end in a semi-colon.
4. We use curly braces {} to denote the beginning and end of 
a class or a method (method == function). 

5. Java is statically typed:
 - Variables must be declared as having a specific type!
 - Those types are permanent!!
*/